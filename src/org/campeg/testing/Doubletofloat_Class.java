package org.campeg.testing;

public class Doubletofloat_Class {

	public static void main(String[] args) {

		Double a = 48765456785.98798765434567890987654;
		System.out.println("Double value:"+a);
		float d = a.floatValue();
		System.out.println(d);

		Double s = 12.75;
		System.out.println("Double:"+s);
		float floatValue = s.floatValue();
		System.out.println(floatValue);

	}

}
